package circlesAndIntersections;

public class Coordinates {
	private int x, y;
	// Constructor method
	public Coordinates(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	// Getters & Setters
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
}